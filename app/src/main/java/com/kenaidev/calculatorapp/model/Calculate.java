package com.kenaidev.calculatorapp.model;

public class Calculate {
    public double soma(int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;
    }

    public double subtracao(int numeroUm, int numeroDois) {
        return numeroUm - numeroDois;
    }

    public double divisao(double numeroUm, double numeroDois) {
        return numeroUm / numeroDois;
    }

    public double multiplicacao(int numeroUm, int numeroDois) {
        return numeroUm * numeroDois;
    }
}
