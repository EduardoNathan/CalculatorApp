package com.kenaidev.calculatorapp.model;

public class Calculate {
    public double sum(int firstNumber, int secondNumber) {

        return firstNumber + secondNumber;
    }

    public double subtraction(int firstNumber, int secondNumber) {
        return firstNumber - secondNumber;
    }

    public double division(double firstNumber, double secondNumber) {
        return firstNumber / secondNumber;
    }

    public double multiplication(int firstNumber, int secondNumber) {
        return firstNumber * secondNumber;
    }
}
